const String notificationIdCounterKey =
    'notificationIdCounter'; // Key ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÄ‡∏Å‡πá‡∏ö ID ‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏•‡πà‡∏≤‡∏™‡∏∏‡∏î

/// ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô‡∏ö‡∏£‡∏¥‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô
/// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ô‡∏µ‡πâ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö Android ‡πÅ‡∏•‡∏∞‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô
/// FlutterLocalNotificationsPlugin ‡∏ô‡∏≠‡∏Å‡∏à‡∏≤‡∏Å‡∏ô‡∏µ‡πâ‡∏¢‡∏±‡∏á‡∏Å‡∏≥‡∏´‡∏ô‡∏î callback ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö
/// ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡πÑ‡∏î‡πâ‡∏£‡∏±‡∏ö‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô
Future<void> initializeNotificationService() async {
  const AndroidInitializationSettings androidSettings =
      AndroidInitializationSettings('@mipmap/ic_launcher');

  const InitializationSettings initSettings =
      InitializationSettings(android: androidSettings);

  await flutterLocalNotificationsPlugin.initialize(
    initSettings,
    onDidReceiveNotificationResponse: (NotificationResponse response) {
      // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å payload ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡πÅ‡∏ï‡∏∞‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô
      LogService.logger.i('Notification payload: ${response.payload}');
    },
  );

  // ‡∏£‡πâ‡∏≠‡∏á‡∏Ç‡∏≠‡∏™‡∏¥‡∏ó‡∏ò‡∏¥‡πå‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö Android (API 33+)
  final AndroidFlutterLocalNotificationsPlugin? androidImplementation =
      flutterLocalNotificationsPlugin.resolvePlatformSpecificImplementation<
          AndroidFlutterLocalNotificationsPlugin>();

  if (androidImplementation != null) {
    final bool? granted =
        await androidImplementation.requestNotificationsPermission();
    if (granted != null) {
      LogService.logger.i('Notification permission granted: $granted');
    } else {
      LogService.logger.w('Notification permission request result is null.');
    }
  }
}

/// callback dispatcher ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö Workmanager
/// ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ô‡∏µ‡πâ‡∏à‡∏∞‡∏ñ‡∏π‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡πÉ‡∏ä‡πâ‡πÉ‡∏ô‡πÄ‡∏ö‡∏∑‡πâ‡∏≠‡∏á‡∏´‡∏•‡∏±‡∏á‡πÇ‡∏î‡∏¢ Workmanager
/// ‡πÇ‡∏î‡∏¢‡∏à‡∏∞‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤ task ‡πÄ‡∏õ‡πá‡∏ô task ‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡πÅ‡∏ö‡∏ö‡∏ß‡∏ô‡∏ã‡πâ‡∏≥‡∏´‡∏£‡∏∑‡∏≠‡πÑ‡∏°‡πà ‡∏à‡∏≤‡∏Å‡∏ô‡∏±‡πâ‡∏ô
/// ‡∏à‡∏∞‡πÅ‡∏™‡∏î‡∏á‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô
@pragma('vm:entry-point') // ‡∏ö‡∏±‡∏á‡∏Ñ‡∏±‡∏ö‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö background tasks
void callbackDispatcher() {
  Workmanager().executeTask((task, inputData) async {
    LogService.logger.i("Workmanager task: $task started.");
    final prefs = await SharedPreferences.getInstance();

    if (task == periodicTask) {
      // ‡∏î‡∏∂‡∏á ID ‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏•‡πà‡∏≤‡∏™‡∏∏‡∏î‡πÅ‡∏•‡∏∞‡πÄ‡∏û‡∏¥‡πà‡∏°‡∏Ñ‡πà‡∏≤
      int currentNotificationId = prefs.getInt(notificationIdCounterKey) ?? 0;
      currentNotificationId++;
      await prefs.setInt(notificationIdCounterKey, currentNotificationId);

      final String randomMessage = getRandomNotificationMessage();

      await flutterLocalNotificationsPlugin.show(
        currentNotificationId, // ‡πÉ‡∏ä‡πâ ID ‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡∏ã‡πâ‡∏≥‡∏Å‡∏±‡∏ô
        '‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏õ‡∏£‡∏∞‡∏à‡∏≥ (‡πÄ‡∏ö‡∏∑‡πâ‡∏≠‡∏á‡∏´‡∏•‡∏±‡∏á)',
        randomMessage,
        const NotificationDetails(
          android: AndroidNotificationDetails(
            channelId,
            channelName,
            channelDescription: channelDescription,
            importance: Importance.high,
            priority: Priority.high,
            // ‡πÄ‡∏û‡∏¥‡πà‡∏° allowWhileIdle ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÉ‡∏´‡πâ‡πÅ‡∏™‡∏î‡∏á‡πÅ‡∏°‡πâ‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏≠‡∏¢‡∏π‡πà‡πÉ‡∏ô‡πÇ‡∏´‡∏°‡∏î‡∏õ‡∏£‡∏∞‡∏´‡∏¢‡∏±‡∏î‡∏û‡∏•‡∏±‡∏á‡∏á‡∏≤‡∏ô
            // ‡πÄ‡∏û‡∏¥‡πà‡∏° fullScreenIntent ‡∏´‡∏≤‡∏Å‡∏ï‡πâ‡∏≠‡∏á‡∏Å‡∏≤‡∏£‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡πÅ‡∏ö‡∏ö‡πÄ‡∏ï‡πá‡∏°‡∏´‡∏ô‡πâ‡∏≤‡∏à‡∏≠
          ),
        ),
        payload: 'payload_from_workmanager_$currentNotificationId',
      );
      // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï timestamp ‡∏•‡πà‡∏≤‡∏™‡∏∏‡∏î‡πÄ‡∏°‡∏∑‡πà‡∏≠ Workmanager ‡∏™‡πà‡∏á‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô
      await prefs.setInt(
          lastScheduledTimestampKey, DateTime.now().millisecondsSinceEpoch);
      LogService.logger
          .i("Periodic notification shown with ID: $currentNotificationId");

      // ‡∏™‡πà‡∏ß‡∏ô‡∏ô‡∏µ‡πâ‡∏à‡∏∞‡∏ñ‡∏π‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡πÉ‡∏ä‡πâ‡πÄ‡∏°‡∏∑‡πà‡∏≠ periodicTask ‡∏ñ‡∏π‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏Å (‡∏£‡∏ß‡∏°‡∏ñ‡∏∂‡∏á‡πÄ‡∏°‡∏∑‡πà‡∏≠ Workmanager ‡∏£‡∏±‡∏ô‡∏ï‡∏≠‡∏ô‡∏ö‡∏π‡∏ï‡πÅ‡∏•‡∏∞‡∏ñ‡∏∂‡∏á‡∏£‡∏≠‡∏ö‡∏Ç‡∏≠‡∏á periodicTask)
      LogService.logger.i(
          "Checking for missed notifications within periodicTask callback...");
      final scheduled = prefs.getBool('isScheduled') ?? false;

      if (scheduled) {
        final lastTimestamp = prefs.getInt(lastScheduledTimestampKey);
        if (lastTimestamp != null) {
          final lastScheduledDateTime =
              DateTime.fromMillisecondsSinceEpoch(lastTimestamp);
          final now = DateTime.now();

          // ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏ó‡∏µ‡πà‡∏û‡∏•‡∏≤‡∏î‡πÑ‡∏õ
          // ‡πÉ‡∏ä‡πâ‡∏Ñ‡πà‡∏≤ interval ‡∏à‡∏≤‡∏Å‡∏î‡πâ‡∏≤‡∏ô‡∏ö‡∏ô‡∏™‡∏∏‡∏î‡∏Ç‡∏≠‡∏á‡πÑ‡∏ü‡∏•‡πå (const Duration(minutes: 15))
          final int missedIntervals =
              (now.difference(lastScheduledDateTime).inSeconds /
                      const Duration(minutes: 15).inSeconds)
                  .floor();

          if (missedIntervals > 0) {
            LogService.logger.i(
                '‡∏û‡∏ö‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏ó‡∏µ‡πà‡∏û‡∏•‡∏≤‡∏î‡πÑ‡∏õ‡∏à‡∏≤‡∏Å periodicTask (‡∏≠‡∏≤‡∏à‡πÄ‡∏Å‡∏¥‡∏î‡∏à‡∏≤‡∏Å boot/downtime): $missedIntervals ‡∏Ñ‡∏£‡∏±‡πâ‡∏á');
            await _triggerMissedNotifications(missedIntervals);

            // ‡∏≠‡∏±‡∏õ‡πÄ‡∏î‡∏ï lastScheduledTimestampKey ‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô‡πÄ‡∏ß‡∏•‡∏≤‡∏ó‡∏µ‡πà‡∏Ñ‡∏ß‡∏£‡∏à‡∏∞‡∏°‡∏µ‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏Ñ‡∏£‡∏±‡πâ‡∏á‡∏•‡πà‡∏≤‡∏™‡∏∏‡∏î‡∏ó‡∏µ‡πà‡∏ñ‡∏π‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡πÉ‡∏ä‡πâ
            final newLastScheduledTime = lastScheduledDateTime.add(Duration(
                seconds:
                    missedIntervals * const Duration(minutes: 15).inSeconds));
            await prefs.setInt(lastScheduledTimestampKey,
                newLastScheduledTime.millisecondsSinceEpoch);
          }
        }
      }
    } else if (task == Workmanager.iOSBackgroundTask) {
      // <<< ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ: ‡∏•‡∏ö androidBootTaskName
      // Workmanager.iOSBackgroundTask ‡∏à‡∏∞‡∏ñ‡∏π‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡πÄ‡∏°‡∏∑‡πà‡∏≠ iOS ‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡πÉ‡∏´‡πâ‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡πÄ‡∏ö‡∏∑‡πâ‡∏≠‡∏á‡∏´‡∏•‡∏±‡∏á
      LogService.logger.i(
          "iOS Background task detected. Checking for missed notifications...");
      final scheduled = prefs.getBool('isScheduled') ?? false;

      if (scheduled) {
        final lastTimestamp = prefs.getInt(lastScheduledTimestampKey);
        if (lastTimestamp != null) {
          final lastScheduledDateTime =
              DateTime.fromMillisecondsSinceEpoch(lastTimestamp);
          final now = DateTime.now();

          final int missedIntervals =
              (now.difference(lastScheduledDateTime).inSeconds /
                      const Duration(minutes: 15).inSeconds)
                  .floor();

          if (missedIntervals > 0) {
            LogService.logger.i(
                '‡∏û‡∏ö‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏ó‡∏µ‡πà‡∏û‡∏•‡∏≤‡∏î‡πÑ‡∏õ‡∏à‡∏≤‡∏Å iOS background task: $missedIntervals ‡∏Ñ‡∏£‡∏±‡πâ‡∏á');
            await _triggerMissedNotifications(missedIntervals);

            final newLastScheduledTime = lastScheduledDateTime.add(Duration(
                seconds:
                    missedIntervals * const Duration(minutes: 15).inSeconds));
            await prefs.setInt(lastScheduledTimestampKey,
                newLastScheduledTime.millisecondsSinceEpoch);
          }
        } else {
          LogService.logger.i(
              'iOS task was scheduled but no last timestamp found, resetting.');
          await prefs.setInt(
              lastScheduledTimestampKey, DateTime.now().millisecondsSinceEpoch);
        }
      }
    }
    return Future.value(true); // ‡∏£‡∏∞‡∏ö‡∏∏‡∏ß‡πà‡∏≤ task ‡πÄ‡∏™‡∏£‡πá‡∏à‡∏™‡∏°‡∏ö‡∏π‡∏£‡∏ì‡πå
  });
}

// Helper function ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö trigger missed notifications (‡∏¢‡πâ‡∏≤‡∏¢‡∏°‡∏≤‡∏≠‡∏¢‡∏π‡πà‡∏ô‡∏≠‡∏Å class)
// ‡πÄ‡∏ô‡∏∑‡πà‡∏≠‡∏á‡∏à‡∏≤‡∏Å callbackDispatcher ‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡πÉ‡∏ô isolate ‡πÅ‡∏¢‡∏Å ‡∏ï‡πâ‡∏≠‡∏á‡πÄ‡∏Ç‡πâ‡∏≤‡∏ñ‡∏∂‡∏á‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÑ‡∏î‡πâ
Future<void> _triggerMissedNotifications(int count) async {
  final prefs = await SharedPreferences.getInstance();
  int currentNotificationId = prefs.getInt(notificationIdCounterKey) ?? 0;

  for (int i = 1; i <= count; i++) {
    currentNotificationId++; // ‡πÄ‡∏û‡∏¥‡πà‡∏° ID ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÅ‡∏ï‡πà‡∏•‡∏∞‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏¢‡πâ‡∏≠‡∏ô‡∏´‡∏•‡∏±‡∏á
    await prefs.setInt(
        notificationIdCounterKey, currentNotificationId); // ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å ID ‡πÉ‡∏´‡∏°‡πà

    final String randomMessage = getRandomNotificationMessage();
    await flutterLocalNotificationsPlugin.show(
      currentNotificationId, // ‡πÉ‡∏ä‡πâ ID ‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡∏ã‡πâ‡∏≥‡∏Å‡∏±‡∏ô
      '‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏¢‡πâ‡∏≠‡∏ô‡∏´‡∏•‡∏±‡∏á‡∏ó‡∏µ‡πà‡∏û‡∏•‡∏≤‡∏î‡πÑ‡∏õ ($i/$count)',
      randomMessage,
      const NotificationDetails(
        android: AndroidNotificationDetails(
          channelId,
          channelName,
          channelDescription: channelDescription,
          importance: Importance.high,
          priority: Priority.high,
        ),
      ),
      payload: 'payload_missed_notification_$currentNotificationId',
    );
    LogService.logger.i(
        '‡πÅ‡∏™‡∏î‡∏á‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡∏¢‡πâ‡∏≠‡∏ô‡∏´‡∏•‡∏±‡∏á‡∏ó‡∏µ‡πà‡∏û‡∏•‡∏≤‡∏î‡πÑ‡∏õ‡∏Ñ‡∏£‡∏±‡πâ‡∏á‡∏ó‡∏µ‡πà $i, ID: $currentNotificationId');
  }
}

/// ‡∏ï‡∏±‡πâ‡∏á‡πÄ‡∏ß‡∏•‡∏≤‡∏Å‡∏≤‡∏£‡πÅ‡∏à‡πâ‡∏á‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô‡πÅ‡∏ö‡∏ö‡∏ß‡∏ô‡∏ã‡πâ‡∏≥‡πÇ‡∏î‡∏¢‡πÉ‡∏ä‡πâ Workmanager
